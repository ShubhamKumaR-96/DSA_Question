// 2744. Find Maximum Number of String Pairs

#include <iostream>
#include <unordered_set>
#include <vector>
using namespace std;


int main()
{
    int n;
    cout << "Enter n : ";
    cin >> n;
    vector<string> v;
    for (int i = 0; i < n; i++)
    {
        string val;
        cin >> val;
        v.push_back(val);
    }
}